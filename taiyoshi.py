# ------------------------------------------------------------
# 9. 二階の窓・障子風
# ------------------------------------------------------------

for x in [-14, -6, 6, 14]:
    fill(x - 2, 16, -17, x + 2, 20, -17, PAPER)
    fill(x, 16, -18, x, 20, -18, LOG)
    fill(x - 2, 18, -18, x + 2, 18, -18, LOG)

